"""
schema.py — 상황보고 스키마.

harness 의 '제약'은 여기서 시작한다.
검증 게이트들이 전부 이 스키마를 참조하므로, 스키마가 곧 계약(contract)이다.
"""

# 상황보고 필수 6항목. 하나라도 비면 하드 게이트가 반려한다.
# (현직 소방관 확정 — 한국 사고 상황보고 표준 항목)
REQUIRED_FIELDS = [
    "발생일시",
    "장소",
    "사고개요",
    "인명피해",
    "동원현황",
    "조치사항",
]

# 현장 활동 시간 순서. 뒤 단계가 앞 단계보다 빠르면 시간 모순 → 하드 게이트 반려.
# 파편 입력이라 일부 시각은 비어 있을 수 있고, 결측은 여기서 막지 않는다
# (존재하는 시각들끼리의 '순서 위반'만 본다).
TIMELINE_ORDER = [
    "신고접수",
    "출동",
    "현장도착",
    "초진",
    "완진",
    "상황종료",
]


def empty_report():
    """스키마 형태의 빈 보고서 dict."""
    return {
        **{f: "" for f in REQUIRED_FIELDS},
        "시각": {k: "" for k in TIMELINE_ORDER},  # ISO 문자열 or ""
    }
